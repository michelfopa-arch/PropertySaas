    public static string Get(string key, string? language = null)
    {
        var dictionary = ResolveDictionary(language);
        if (dictionary.TryGetValue(key, out var value))
        {
            return value;
        }

        return English.TryGetValue(key, out var fallback) ? fallback : key;
    }

    public static string Format(string key, string? language = null, params object[] args)
    {
        return string.Format(Get(key, language), args);
    }

    private static IReadOnlyDictionary<string, string> ResolveDictionary(string? language)
    {
        var culture = string.IsNullOrWhiteSpace(language) ? CultureInfo.CurrentUICulture.Name : language;
        if (culture.StartsWith("fr", StringComparison.OrdinalIgnoreCase))
        {
            return French;
        }

        if (culture.StartsWith("es", StringComparison.OrdinalIgnoreCase))
        {
            return Spanish;
        }

        return English;
    }
